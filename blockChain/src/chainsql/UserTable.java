package chainsql;

public class UserTable {
    String id;
    String name;
    String password;
    String group;
    String admin;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getPassword() {
        return password;
    }

    public String getGroup() {
        return group;
    }

    public String getAdmin() {
        return admin;
    }
}
